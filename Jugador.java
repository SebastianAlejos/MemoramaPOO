public class Jugador {
private String nombre;
private int score;
    public Jugador(String nombre){
        this.nombre=nombre;
        this.score=0;
    }
    public void setScore(int score){
        this.score=score;
    }
    public String getNombre(){
        return this.score;
    }
    public int getScore(){
        return this.score;
    }
}